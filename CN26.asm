 ;write assembly program to accept binary input
;and display equivalent hexa output

.MODEL SMALL
.STACK 100H
.DATA
D1 DB "ENTER A BINARY NUMBER: $"
D2 DB 10,12,"HEXA NUMBER IS : $"
.CODE
MAIN PROC 
    MOV AX,@DATA
    MOV DS,AX
    LEA DX,D1 
MOV AH,9
INT 21H

MOV BX,BX
MOV AH,1
INT 21H

WHILE:
CMP AL,0DH
JE END_WHILE
AND AL,0FH
SHL BX,1
OR BL,AL
INT 21H
JMP WHILE
END_WHILE:
MOV AH,9
LEA DX,D2
INT 21H
MOV AH,2
MOV CL,4

TOP:
CMP CL,0
JE EXIT

ROL BX,4
MOV DL,BL
AND DL,0FH
DEC CL

CMP DL,9
JG LETTER
JLE DIGIT

LETTER:
ADD DL,37H
INT 21H
JMP TOP
DIGIT:
ADD DL,30H
INT 21H
JMP TOP
EXIT:
MAIN ENDP
END MAIN


