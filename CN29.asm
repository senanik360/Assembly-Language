.CODE
MAIN PROC
    
MOV BX,0100110010101001B
MOV CX,4

TOP: 

MOV DL,BH

MOV CH,4
SHIFT:
SHR DL,1
DEC CH
JNZ SHIFT 

MOV AH,2

CMP DL,9
JLE ZTN
JMP ATF


ATF:
ADD DL,37H
INT 21H 
